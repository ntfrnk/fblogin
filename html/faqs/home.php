			<h3>¿Qué es el «Curso de Formación de Preventores del Abuso Sexual Infantil?</h3>
			<p>Es un curso intensivo con formación teórica y práctica en cuanto al abuso sexual infantil y su prevención.</p>

			<h3>¿Cómo es la modalidad del curso?</h3>
			<p>La modalidad es 100% online y permite a los participantes adquirir las herramientas teóricas y prácticas de la prevención de forma virtual. A través de esta modalidad también aprenderán a aplicar el Programa Educativo Integral "Mientras nos divertimos aprendemos a vivir".</p>

			<h3>¿Para quién está pensado el curso?</h3>
			<p>El curso está pensado para todas aquellas personas que quieran capacitarse para detectar, visibilizar y prevenir el abuso sexual infantil en su entorno. Padres, madres, docentes, profesionales de    la    salud,    abogados,   psicólogos,    psicólogos sociales, estudiantes mayores de 18 años y la comunidad en general.</p>

			<h3>¿Qué temas abarca el curso?</h3>
			<p>El  curso  posee  contenidos  teóricos  y  prácticos.  Temas  como: Qué es el abuso sexual infantil, cómo detectarlo, qué hacer ante un  abuso  sexual,  perfil  del  abusador,  secuelas,  etc.  serán dictados durante el curso. Cómo así también: Prevención, tipos de prevención, normas de seguridad, metodología didáctica de la  prevención,  manejo  del  programa  de  prevención  de  abuso "Mientras nos divertimos aprendemos a vivir".</p>

